namespace DSPCore;

/// <summary>
/// 定义 DSPCore 支持的选择器类型。
/// Defines picker kinds supported by DSPCore.
/// </summary>
public enum PickerKind
{
    /// <summary>
    /// 物品选择器。
    /// Item picker.
    /// </summary>
    Item,

    /// <summary>
    /// 配方选择器。
    /// Recipe picker.
    /// </summary>
    Recipe,

    /// <summary>
    /// 信号或图标选择器。
    /// Signal or icon picker.
    /// </summary>
    Signal
}
